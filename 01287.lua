function LuaN_112581_Parade() --兔
	local rabbit={}
	for i = 0,19 do  --20隻兔
		rabbit[i] = CreateObjByFlag(112581,780301, i ,1) --使用旗子產生兔子 npc 旗子編號 數量 
		AddToPartition( rabbit[i],0 )	--讓兔子產生
--		Say( rabbit[i] , i )
		SetRandMove(rabbit[i],60, 100, 15 )	--兔子亂數範圍內移動  (150=範圍, 100=10秒 , 20=每次移動距離)
	end
end
